#include "libft.h"

int	ft_isblank(int c)
{
	return ((c == ' ') || (c == '\t') || (c == '\v'));
}

int	ft_isspacbk(int c)
{
	return (ft_isblank(c) || (c == '\f') || (c == '\n') || (c == '\r'));
}
